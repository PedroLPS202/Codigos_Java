public class DeclarandoVarieveisPORTUGUES {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;

        int soma = a + b; 
        int difença = a - b;
        int produto = a * b; 
        int quociente = a / b;

        System.out.println("Soma:" + soma);
        System.out.println ("Diferença:" + difença);
        System.out.println ("Produto:" + produto);
        System.out.println ("Quociente:" + quociente);
    }
}